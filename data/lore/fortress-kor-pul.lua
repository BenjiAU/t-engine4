	name = "Chronicles of Kor'pul (2)",
	lore = [[#{italic}#It appears to be a page fallen out of a book. The handwriting is archaic, but clean and readable.#{normal}#
	
	Grandfather was long dead and buried when I first felt it. After he was gone, I took up his work and dug the graves. It was hard work, but it was the only work I could find. And in some way I was grateful for the ancient stigma that clung to my family, for this way I could spend my days alone and in peace. Very few people ever came to the graveyard, and those who did merely nodded their heads in silence when they met my gaze.
	
	I was out among the graves, thinking about life and death, when I felt the tugging from beneath. It was as though someone, or something, was calling for me. It took me a while to understand that it was the dead themselves, or rather the faint traces of what used to be life. Using the arcane forces that I now knew to be mine, I turned to the greatest of all arcane disciplines, the art of necromancy. If I could bring those back who had died long ago, would I also be able to sustain my own existence indefinitely?
	
	I had to try. Material I had enough, and I knew that the souls of the dead would heed my commands. In the dead of the night, I sneaked out into the graveyard, and began my work. Soon, a coffin was unearthed, and I smiled as I beheld the mortal remains inside. So well preserved! It was promising, to say the least.
	
	That was the night old Mok'rol rose from his grave and walked the earth again. It was also the last night I spent in the village of my youth, for in the morning the men came to my cottage with their torches and pitchforks. 
	
	]],
}

