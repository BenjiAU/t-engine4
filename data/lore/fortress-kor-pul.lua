	The Shaloren are a curious people. Ever since the Spellblaze, they have been hidden from the world that blamed them for it. The elders said that the Shaloren are murderous spell-weavers who would rip all of Eyal apart if we would let them, and they said that it is the duty of every man to kill them on sight. It is the same reaction they showed to my research. So it was oddly fitting that one of their former places of power should harbour me and my work. 
	
	It was an old outpost, long abandoned by the elves who had built it in the Age of Allure. Not far away from the town of Derth, yet safely out of the reach of the townsfolk who refused to go near it. After all, the Shaloren had built it, and nothing good ever came of their works. I found a room that would serve well as a library, and the dining hall with its table was quickly converted to an excellent operating room. 
	
	Soon I managed to get the first specimens from the graveyard of Derth. Such joy, to be able to work without being disturbed! I made a number of skeletal guardians and ordered them to patrol the fortress. They would serve just fine to repel any nosy adventurer who might stumble upon this place. Now I could concentrate on calling up some more powerful undead, and finally begin my research into the ultimate prize of necromancy: to achieve immortality!
	
	]],
}

