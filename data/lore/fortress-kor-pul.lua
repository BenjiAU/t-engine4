	lore = [[#{italic}#It appears to be a page fallen out of a book. The handwriting is archaic, but clean and readable.#{normal}#
	
	I remember the smell of the wet earth after the autumn rains. We walked across the graveyard, grandfather leading the way between the scattered gravestones. The large, crudely chiseled rocks that jutted out of the earth appeared dark and threatening to me. I was still very young and could not yet read the runes hewn into them. But I knew that they told of what was gone, long gone below the earth. And so I stared, a strange foreboding sensation gripping my heart, while grandfather did his work. Long he dug, and deep, until the hole was made. Then the men came and put the large wooden box into the earth. 
	
	I was young, but I knew what the box meant. Grandfather had explained to me what it was he was doing. The old woman was dead, he had said. She was gone, her soul perhaps dancing among the stars, but the body remained. And it was the custom among our people to take the body and put it in the earth it came from. All people would die someday, and his task was to dig the holes in the earth. His words would echo in my head for a long time afterwards. They were all I could think of as I stood among the gravestones. All people would die someday. 
	
	And the stones glared at me with their graven runes, and I knew what was to come. I would grow old and die, and the men would come and put me in a wooden box in the earth, and they would put a stone above my head. I could barely breathe, my thoughts racing in my head. Never again would I see the flowers of spring, or the verdant glory of summer, or the blazing colours of the autumn leaves. 
	
	I was relieved when grandfather came and brought me away that day, away from the stones and all they stood for. But I never forgot the doom that hung over me and all of my kind. From this day onward, I would do everything I could to escape it.
	
	]],
}

