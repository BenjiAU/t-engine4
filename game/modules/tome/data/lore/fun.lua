newLore{
	id = "troll-poem-1",
	category = "misc",
	name = "trollish poem (part one)",
	lore = [[#{bold}#"Me Likes to Club"

#{italic}#Club club club,
Grab some grub!
When hunger hits,
Me likes to club!

Little halfling,
Furry feet,
Smash his brains,
Tasty meat!

Skinny elf,
Eyes so blue,
Crush his bones,
Make elf stew!

...#{normal}#]],
}

newLore{
	id = "troll-poem-2",
	category = "misc",
	name = "trollish poem (part two)",
	lore = [[#{italic}#...

Hairy dwarf,
Smells like dirt,
Cook him long,
Or stomach hurt!

Stupid human,
Has no fear,
Looks surprised,
When me eats his ear!

Club club club,
Grab some grub!
When hunger hits,
Me likes to club!#{normal}#]],
}
