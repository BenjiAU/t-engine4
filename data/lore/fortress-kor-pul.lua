	The elf on the operating table quivered in fear. It was very uncommon for the Shaloren to wander outside their hidden cities, but rarely one of the younger or more adventurous elves decided to explore the outside world in disguise. This particular one had the misfortune of wandering into my sphere of influence, and I can see with more than just my eyes. The undead under my control swiftly captured him and brought him to my fortress.
	
	It is said that the Shaloren have achieved immortality, but I knew this to be untrue. Yes, they cannot die of old age. But they can still be slain by sword or spell - or, in this case, by a scalpel. After most of the screaming had stopped, I started to dissect the poor fellow in order to uncover the secret that might help me to achieve my goal. Following traces of arcane energies throughout his body, I eventually discovered the secret of the Shaloren.
	
	Using what I had learned from the vivisection, and drawing upon ancient descriptions of the Lichdom ceremony, I started to devise a ritual that would bind my soul to the very fabric of reality. This way, even the most powerful mages would not be able to defeat me permanently! Phylacteries can be shattered, arcane embalmings can be transmuted, runes of resurrection can be overwritten. But you cannot banish someone who is bound to reality itself.
	
	]],
}

